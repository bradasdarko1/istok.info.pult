function cyrillicToLatin(text: string) {
  const multiMap: Record<string, string> = {
    Љ: 'LJ',
    љ: 'lj',
    Њ: 'NJ',
    њ: 'nj',
    Џ: 'DŽ',
    џ: 'dž',
  }

  let result = text

  Object.entries(multiMap).forEach(([cyr, lat]) => {
    result = result.split(cyr).join(lat)
  })

  const singleMap: Record<string, string> = {
    А: 'A',
    а: 'a',
    Б: 'B',
    б: 'b',
    В: 'V',
    в: 'v',
    Г: 'G',
    г: 'g',
    Д: 'D',
    д: 'd',
    Ђ: 'Đ',
    ђ: 'đ',
    Е: 'E',
    е: 'e',
    Ж: 'Ž',
    ж: 'ž',
    З: 'Z',
    з: 'z',
    И: 'I',
    и: 'i',
    Ј: 'J',
    ј: 'j',
    К: 'K',
    к: 'k',
    Л: 'L',
    л: 'l',
    М: 'M',
    м: 'm',
    Н: 'N',
    н: 'n',
    О: 'O',
    о: 'o',
    П: 'P',
    п: 'p',
    Р: 'R',
    р: 'r',
    С: 'S',
    с: 's',
    Т: 'T',
    т: 't',
    Ћ: 'Ć',
    ћ: 'ć',
    У: 'U',
    у: 'u',
    Ф: 'F',
    ф: 'f',
    Х: 'H',
    х: 'h',
    Ц: 'C',
    ц: 'c',
    Ч: 'Č',
    ч: 'č',
    Ш: 'Š',
    ш: 'š',
  }

  return result
    .split('')
    .map((char) => singleMap[char] ?? char)
    .join('')
}

